package com.itbank.food;

public class ProductDTO {

	int proId;
	String proName;
	int proPrice;
	String proSeller;
	String proContent;
	String proImg;
	String proExpDate;
	int proQuantity;
	int proHit;

	public int getProId() {
		return proId;
	}

	public void setProId(int proId) {
		this.proId = proId;
	}

	public String getProName() {
		return proName;
	}

	public void setProName(String proName) {
		this.proName = proName;
	}

	public int getProPrice() {
		return proPrice;
	}

	public void setProPrice(int proPrice) {
		this.proPrice = proPrice;
	}

	public String getProSeller() {
		return proSeller;
	}

	public void setProSeller(String proSeller) {
		this.proSeller = proSeller;
	}

	public String getProContent() {
		return proContent;
	}

	public void setProContent(String proContent) {
		this.proContent = proContent;
	}

	public String getProImg() {
		return proImg;
	}

	public void setProImg(String proImg) {
		this.proImg = proImg;
	}

	public String getProExpDate() {
		return proExpDate;
	}

	public void setProExpDate(String proExpDate) {
		this.proExpDate = proExpDate;
	}

	public int getProQuantity() {
		return proQuantity;
	}

	public void setProQuantity(int proQuantity) {
		this.proQuantity = proQuantity;
	}

	public int getProHit() {
		return proHit;
	}

	public void setProHit(int proHit) {
		this.proHit = proHit;
	}

}
